import { execFileSync } from 'node:child_process'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const outFile = path.join(root, 'docs/release/production-approval-dossier.md')

function readJson(relativePath) {
  return JSON.parse(fs.readFileSync(path.join(root, relativePath), 'utf8'))
}

function table(rows, headers) {
  const header = `| ${headers.join(' | ')} |`
  const divider = `| ${headers.map(() => '---').join(' | ')} |`
  const body = rows.map((row) => `| ${headers.map((key) => String(row[key] ?? '').replace(/\n/g, ' ')).join(' | ')} |`)
  return [header, divider, ...body].join('\n')
}

const assets = readJson('public/data/assets.json')
const mapArt = readJson('public/data/map-art.json')
const audioSources = readJson('public/data/audio-sources.json')
const levelPlan = readJson('public/data/level-production-plan.json')
const bosses = readJson('public/data/bosses.json')
const releaseReport = JSON.parse(execFileSync('node', ['tools/check-release-readiness.mjs'], { cwd: root, encoding: 'utf8' }))

const approvedAudio = Object.entries(audioSources.cues || {}).map(([key, cue]) => ({
  key,
  file: cue.file,
  status: cue.approvalStatus,
  source: cue.source,
  license: cue.license,
  commercialUse: cue.commercialUse,
}))

const blockedTextures = Object.entries(assets.textures || {})
  .filter(([key]) => releaseReport.blockers.some((blocker) => blocker.kind === 'texture' && blocker.key === key))
  .map(([key, asset]) => ({
    key,
    file: asset.file,
    status: asset.status,
    license: asset.license,
    commercialUse: asset.commercialUse,
    action: asset.notes || 'Replace or formally approve before production release.',
  }))

const blockedMapArt = (mapArt.assets || [])
  .filter((asset) => releaseReport.blockers.some((blocker) => blocker.kind === 'map-art' && blocker.key === asset.key))
  .map((asset) => ({
    key: asset.key,
    map: asset.map,
    status: asset.approvalStatus,
    license: asset.license,
    commercialUse: asset.commercialUse,
    action: asset.reviewNotes,
  }))

const levelRows = (levelPlan.levels || []).map((level) => ({
  order: level.order,
  id: level.id,
  status: level.productionStatus,
  boss: level.boss?.id || 'missing',
  action: level.nextHarnessStep,
}))

const sharedBossSheets = releaseReport.blockers
  .filter((blocker) => blocker.kind === 'boss-art')
  .map((blocker) => ({
    texture: blocker.key,
    reason: blocker.reason,
    action: 'Create or import a unique approved boss sprite sheet and update public/data/bosses.json plus public/data/assets.json.',
  }))

const bossRows = (bosses.bosses || []).map((boss) => ({
  id: boss.id,
  texture: boss.texture,
  hp: boss.hp,
  phases: boss.phases?.length || 0,
  status: sharedBossSheets.some((row) => row.texture === boss.texture) ? 'shared-sheet-blocked' : 'unique-texture-slot',
}))

const body = `# Production Approval Dossier

Generated by \`npm run release:dossier\`.

## Release Readiness

- release ready: ${releaseReport.releaseReady ? 'yes' : 'no'}
- blocker count: ${releaseReport.blockerCount}
- approved audio cues: ${approvedAudio.length}
- blocked texture assets: ${blockedTextures.length}
- blocked map-art assets: ${blockedMapArt.length}
- level-production blockers: ${releaseReport.blockers.filter((blocker) => blocker.kind === 'level-production').length}
- boss-art blockers: ${sharedBossSheets.length}

## Approved Audio

${table(approvedAudio, ['key', 'file', 'status', 'source', 'license', 'commercialUse'])}

## Blocked Texture Assets

${table(blockedTextures, ['key', 'file', 'status', 'license', 'commercialUse', 'action'])}

## Blocked Map Art

${table(blockedMapArt, ['key', 'map', 'status', 'license', 'commercialUse', 'action'])}

## Level Production Status

${table(levelRows, ['order', 'id', 'status', 'boss', 'action'])}

## Boss Art Replacement Needs

${table(sharedBossSheets, ['texture', 'reason', 'action'])}

## Boss Runtime Tuning Snapshot

${table(bossRows, ['id', 'texture', 'hp', 'phases', 'status'])}

## Production Rule

The game may not be called production-ready until \`npm run release:check\` exits successfully and every row above has source, author, license, commercial-use permission, and human approval where required.
`

fs.mkdirSync(path.dirname(outFile), { recursive: true })
fs.writeFileSync(outFile, body)
console.log(JSON.stringify({ outFile: path.relative(root, outFile), blockerCount: releaseReport.blockerCount }, null, 2))
