import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import { chromium, devices } from 'playwright'
import { inspectMap, loadMap, validateMapContract } from './lib/map-contract.mjs'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')
const args = process.argv.slice(2)

function arg(name, fallback = undefined) {
  const index = args.indexOf(name)
  if (index === -1) return fallback
  return args[index + 1] ?? fallback
}

function ensureDir(dir) {
  fs.mkdirSync(dir, { recursive: true })
}

const mapId = arg('--map-id', 'stage-01-metro-arcade')
const url = arg('--url', 'http://127.0.0.1:4177/')
const captureAll = args.includes('--all')
const reviewsRoot = path.join(root, 'docs/reviews/map-previews')
const artManifest = JSON.parse(fs.readFileSync(path.join(root, 'public/data/map-art.json'), 'utf8'))
const levelProductionPlan = JSON.parse(fs.readFileSync(path.join(root, 'public/data/level-production-plan.json'), 'utf8'))

const profiles = [
  { name: 'desktop', viewport: { width: 1440, height: 900 }, deviceScaleFactor: 1, isMobile: false },
  { name: 'phone', ...devices['iPhone 14'] },
  { name: 'tv', viewport: { width: 1920, height: 1080 }, deviceScaleFactor: 1, isMobile: false },
]

async function captureMap(browser, id) {
  const outDir = path.join(reviewsRoot, id)
  const mapFile = path.join(root, 'public/assets/maps', `${id}.json`)
  const map = loadMap(mapFile)
  const contract = validateMapContract(map)
  const mapReport = inspectMap(map)

  ensureDir(outDir)
  const captured = []
  for (const profile of profiles) {
    console.log(`capturing ${id} ${profile.name}`)
    const context = await browser.newContext({
      ...profile,
      viewport: profile.viewport,
    })
    const page = await context.newPage()
    await page.goto(url)
    await page.waitForSelector('canvas')
    await page.waitForFunction(() => typeof window.__NEON_START_LEVEL__ === 'function')
    await page.evaluate((levelId) => window.__NEON_START_LEVEL__?.(levelId), id)
    await page.waitForFunction(() => !!window.__NEON_DEBUG__?.player)
    await page.waitForFunction((levelId) => window.__NEON_DEBUG__?.level?.id === levelId, id)
    await page.evaluate(() => window.__NEON_FREEZE__?.())
    await page.waitForTimeout(250)
    const file = `${profile.name}.png`
    await page.locator('canvas').screenshot({ path: path.join(outDir, file) })
    captured.push(file)
    console.log(`captured ${id} ${file}`)
    await context.close()
  }
  writeReport(id, captured, contract, mapReport)
  return { mapId: id, outDir: path.relative(root, outDir), captured }
}

function writeReport(id, captured, contract, mapReport) {
  const outDir = path.join(reviewsRoot, id)
  const level = levelProductionPlan.levels.find((candidate) => candidate.id === id)
  const artAssets = artManifest.assets.filter((asset) => asset.map === id || asset.map === 'shared')
  const report = `# Map Preview Review: ${id}

Generated by \`npm run capture:map -- --map-id ${id}\`.

## Captured Screenshots

${captured.map((file) => `- [${file}](${file})`).join('\n')}

Runtime level verified during capture: \`${id}\`.

## Level Production Status

- name: ${level?.name || id}
- order: ${level?.order ?? 'unknown'}
- production status: ${level?.productionStatus || 'unknown'}
- boss: ${level?.boss?.id || 'unknown'}
- next step: ${level?.nextHarnessStep || 'missing'}

## Reference Target

The target quality is the rich scene-plate standard documented in \`docs/HYBRID_MAP_HARNESS_PLAN.md\`: dense illustrated environment, readable actors, no ugly repeated filler as the primary stage.

## Map Contract

- production safe: ${contract.ok ? 'yes' : 'no'}
- scene plates: ${mapReport.scenePlates.join(', ') || 'none'}
- prototype tile layers: ${mapReport.prototypeTileLayers.join(', ') || 'none'}
- visible prototype tile layers: ${mapReport.visiblePrototypeTileLayers.join(', ') || 'none'}
- missing layers: ${mapReport.missingLayers.join(', ') || 'none'}

${contract.errors.length ? `### Contract Errors\n\n${contract.errors.map((error) => `- ${error}`).join('\n')}\n` : ''}
## Art Intake Summary

${artAssets.length ? artAssets.map((asset) => `- \`${asset.key}\` (${asset.role}) - ${asset.approvalStatus}; commercial use: ${asset.commercialUse}`).join('\n') : '- No map-specific art manifest entry. Treat this packet as a stand-in/prototype review until final scene art is ingested.'}

## Human Review Checklist

- First viewport looks like a finished scene, not a pattern wall.
- Player and enemies remain readable.
- Foreground/depth does not cover actors or controls.
- Gameplay lane is visually understandable.
- No ghosted/stretched legacy layer is visible.
- Prototype tile layers are not the primary art.
- Asset source/license status is acceptable for the intended use.

## Review Decision

- status: needs human review
- reviewer:
- notes:
`
  fs.writeFileSync(path.join(outDir, 'README.md'), report)
}

function writeIndex() {
  ensureDir(reviewsRoot)
  const rows = levelProductionPlan.levels
    .map((level) => {
      const packetDir = path.join(reviewsRoot, level.id)
      const files = ['desktop.png', 'phone.png', 'tv.png'].filter((file) => fs.existsSync(path.join(packetDir, file)))
      const hasReadme = fs.existsSync(path.join(packetDir, 'README.md'))
      const packet = hasReadme ? `[packet](${level.id}/README.md)` : 'missing'
      return `| ${level.order} | ${level.id} | ${level.productionStatus} | ${level.boss?.id || 'missing'} | ${packet} | ${files.join(', ') || 'missing'} | ${level.nextHarnessStep || 'missing'} |`
    })
    .join('\n')
  const report = `# Map Preview Reviews

Each subdirectory is a visual review packet for a level/map.

These packets are not automated approval. They are artifacts for human comparison against the target reference quality.

Generated by \`npm run capture:map -- --all\`.

| # | Level | Production status | Boss | Packet | Screenshots | Next step |
|---|---|---|---|---|---|---|
${rows}
`
  fs.writeFileSync(path.join(reviewsRoot, 'README.md'), report)
}

const ids = captureAll ? levelProductionPlan.levels.map((level) => level.id) : [mapId]
const browser = await chromium.launch()
const results = []
try {
  for (const id of ids) {
    results.push(await captureMap(browser, id))
  }
} finally {
  await browser.close()
}
writeIndex()
console.log(JSON.stringify({ captured: results }, null, 2))
